#include <stdio.h>
/**
 * main - This function aims to print out '_putchar' using putchar
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int letter;
	{
		letter = '_';
		putchar(letter);
		letter = 'p';
		putchar(letter);
		letter = 'u';
		putchar(letter);
		letter = 't';
		putchar(letter);
		letter = 'c';
		putchar(letter);
		letter = 'h';
		putchar(letter);
		letter = 'a';
		putchar(letter);
		letter = 'r';
		putchar(letter);
	}
	putchar('\n');
	return (0);
}
